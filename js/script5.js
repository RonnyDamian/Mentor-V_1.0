$('.ancla').on('click', function(e){


    var elemento = $(this);

    var strAncla = '#' + $(this).data('ancla');
    $('html, body').animate({
        scrollTop: $(strAncla).offset().top
    }, 900, function(){
        $(elemento).prop("checked", true);
    });
});
